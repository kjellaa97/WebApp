package com.ezen.web.login2;

public class LoginOboard {

		private String uid;
		private String pwd;
		
		public LoginOboard() {}
		
		public String getUid() {
			return uid;
		}
		public void setUid(String uid) {
			this.uid = uid;
		}
		public String getPwd() {
			return pwd;
		}
		public void setPwd(String pwd) {
			this.pwd = pwd;
		}
	

}
