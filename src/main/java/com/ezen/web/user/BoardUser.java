package com.ezen.web.user;

public class BoardUser 
{
	private String uid;
	private String pwd;
	
	public BoardUser() {}
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
